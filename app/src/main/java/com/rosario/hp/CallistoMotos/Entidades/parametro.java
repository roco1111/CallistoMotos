package com.rosario.hp.CallistoMotos.Entidades;

public class parametro {
    String id;
    String desc_parametro;
    String valor;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDesc_parametro() {
        return desc_parametro;
    }

    public void setDesc_parametro(String desc_parametro) {
        this.desc_parametro = desc_parametro;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public parametro(){
        super();
    }
}
